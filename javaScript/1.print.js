// print 1-5
for (let i = 1; i <= 5; i++) {
    console.log(i);
}

console.log(`-------------------`);


// Print 1–10
let i = 0;
while (i <= 10) {
    console.log(i);
    i++;
}

console.log(`-------------------`);

// Print 5–1
for (let i = 5; i >= 1; i--) {
    console.log(i);
}

console.log(`-------------------`);


// Print 10–1
let n = 10;
while (n >= 1) {
    console.log(n);
    n--;
}

console.log(`-------------------`);
